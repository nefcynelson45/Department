     <h3 id="mca">MCA:</h3>
     <p class="lead">Welcome to the MCA programme of Rajagiri College of Social Sciences, Hill Campus, Kalamassery. <br>
                The three-year Master’s Degree Programme in Computer Applications(MCA)* was introduced by the college in 2001 to meet the growing demand for well qualified and trained computer programmers. The course is affiliated to Mahatma Gandhi University, Kottayam, Kerala, India, and approved by the All India Council for Technical Education, New Delhi. The school endeavours to bring out world-class professionals in the development and use of software for different applications.
                <br>
                The MCA programme offered by Rajagiri College is one of the most sought after courses by students aspiring to become top class computer professionals. The annual intake of students to the course is Forty-Five. The students are admitted to the course through careful screening and other admission criteria.
                <br><br>
                <h5>Programme Outcome (PO) :</h5>
                
                At the end of the Programme, a student will be able to:<br>
                <ul>
                <li><b>PO1</b>:Computational Knowledge: Apply knowledge of computing fundamentals, computing specialisation, mathematics, and domain knowledge appropriate for the computing specialisation to the abstraction and conceptualisation of computing models from defined problems and requirements.</li>
                <li><b>PO2</b>: Problem Analysis: Identify, formulate, research literature, and solve complex computing problems reaching substantiated conclusions using fundamental principles of mathematics, computing sciences, and relevant domain disciplines.</li>
                <li><b>PO3</b>: Design /Development of Solutions: Design and evaluate solutions for complex computing problems, and design and evaluate systems, components, or processes that meet specified needs with appropriate consideration for public health and safety, cultural, societal, and environmental considerations.</li>
                <li><b>PO4</b>: Conduct Investigations of Complex Computing Problems: Use research-based knowledge and research methods including design of experiments, analysis and interpretation of data, and synthesis of the information to provide valid conclusions.</li>
                <li><b>PO5</b>:Modern Tool Usage: Create, select, adapt and apply appropriate techniques, resources, and modern computing tools to complex computing activities, with an understanding of the limitations.</li>
                <li><b>PO6</b>:Professional Ethics: Understand and commit to professional ethics and cyber regulations, responsibilities, and norms of professional computing practice.</li>
                <li><b>PO7</b>: Life-long Learning: Recognise the need, and have the ability, to engage in independent learning for continual development as a computing professional.</li>
                <li><b>PO8</b>: Project management and finance: Demonstrate knowledge and understanding of the computing and management principles and apply these to one’s own work, as a member and leader in a team, to manage projects and in multidisciplinary environments.</li>
                <li><b>PO9</b>: Communication Efficacy: Communicate effectively with the computing community, and with society at large, about complex computing activities by being able to comprehend and write effective reports, design documentation, make effective presentations, and give and understand clear instructions.</li>
                <li><b>PO10</b>:Societal and Environmental Concern: Understand and assess societal, environmental, health, safety, legal, and cultural issues within local and global contexts, and the consequential responsibilities relevant to professional computing practice.</li>
                <li><b>PO11</b>: Individual and Team Work: Function effectively as an individual and as a member or leader in diverse teams and in multidisciplinary environments.</li>
                <li><b>PO12</b>: Innovation and Entrepreneurship: Identify a timely opportunity and using innovation to pursue that opportunity to create value and wealth for the betterment of the individual and society at large.</li>
                </p></ul>
